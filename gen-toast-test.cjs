const fs = require("fs");
const p = "/Users/claudewilliams/Desktop/NOLTCsignup/tests/unit/shared/utils/toast.test.ts";
const L = [];
L.push("/** @vitest-environment jsdom */");
L.push("");
L.push("import { describe, it, expect, beforeEach, afterEach } from 'vitest';");
L.push("import { toast } from '../../../../src/shared/utils/toast';");
L.push("");
L.push("describe('toast', () => {");
L.push("  let events;");
L.push("  let handler;");
L.push("");
L.push("  beforeEach(() => {");
L.push("    events = [];");
L.push("    handler = (e) => events.push(e);");
L.push("    window.addEventListener('UI_TOAST', handler);");
L.push("  });");
L.push("");
L.push("  afterEach(() => {");
L.push("    window.removeEventListener('UI_TOAST', handler);");
L.push("    events = [];");
L.push("  });");
L.push("");
L.push("  it('dispatches UI_TOAST CustomEvent on window', () => {");
L.push("    toast('Hello');");
L.push("    expect(events).toHaveLength(1);");
L.push("    expect(events[0].type).toBe('UI_TOAST');");
L.push("  });");
L.push("");
L.push("  it('sets msg in event detail', () => {");
L.push("    toast('Court assigned');");
L.push("    expect(events[0].detail.msg).toBe('Court assigned');");
L.push("  });");
L.push("");
L.push("  it('includes type option in detail', () => {");
L.push("    toast('Saved', { type: 'success' });");
L.push("    expect(events[0].detail.type).toBe('success');");
L.push("  });");
L.push("");
L.push("  it('includes duration option in detail', () => {");
L.push("    toast('Warning', { duration: 5000 });");
L.push("    expect(events[0].detail.duration).toBe(5000);");
L.push("  });");
L.push("");
L.push("  it('includes both type and duration', () => {");
L.push("    toast('Error', { type: 'error', duration: 3000 });");
L.push("    expect(events[0].detail.msg).toBe('Error');");
L.push("    expect(events[0].detail.type).toBe('error');");
L.push("    expect(events[0].detail.duration).toBe(3000);");
L.push("  });");
L.push("");
L.push("  it('no options - detail has only msg', () => {");
L.push("    toast('Simple');");
L.push("    const d = events[0].detail;");
L.push("    expect(d.msg).toBe('Simple');");
L.push("    expect(d.type).toBeUndefined();");
L.push("    expect(d.duration).toBeUndefined();");
L.push("  });");
L.push("");
L.push("  it('dispatches separate events for multiple calls', () => {");
L.push("    toast('One');");
L.push("    toast('Two');");
L.push("    toast('Three');");
L.push("    expect(events).toHaveLength(3);");
L.push("    expect(events[0].detail.msg).toBe('One');");
L.push("    expect(events[1].detail.msg).toBe('Two');");
L.push("    expect(events[2].detail.msg).toBe('Three');");
L.push("  });");
L.push("");
L.push("  it('supports info type', () => {");
L.push("    toast('Info', { type: 'info' });");
L.push("    expect(events[0].detail.type).toBe('info');");
L.push("  });");
L.push("");
L.push("  it('supports warning type', () => {");
L.push("    toast('Warn', { type: 'warning' });");
L.push("    expect(events[0].detail.type).toBe('warning');");
L.push("  });");
L.push("");
L.push("  it('empty string message dispatches event', () => {");
L.push("    toast('');");
L.push("    expect(events).toHaveLength(1);");
L.push("    expect(events[0].detail.msg).toBe('');");
L.push("  });");
L.push("});");
const NL = String.fromCharCode(10); fs.writeFileSync(p, L.join(NL) + NL);
